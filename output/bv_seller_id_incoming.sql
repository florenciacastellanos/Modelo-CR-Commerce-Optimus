WITH BASE_CONTACTS AS (
    SELECT
        C.CLA_CLAIM_ID,
        C.SIT_SITE_ID,
        DATE_TRUNC(C.CONTACT_DATE_ID, MONTH) AS PERIODO
    FROM `meli-bi-data.WHOWNER.BT_CX_CONTACTS` C
    WHERE C.SIT_SITE_ID IN ('MLB')
        AND C.CONTACT_DATE_ID >= '2025-11-01'
        AND C.CONTACT_DATE_ID < '2026-01-01'
        AND C.PROCESS_BU_CR_REPORTING IN ('ME', 'ML')
        AND COALESCE(C.FLAG_EXCLUDE_NUMERATOR_CR, 0) = 0
        AND C.CLA_CLAIM_ID IS NOT NULL
        AND (
            C.PROCESS_PROBLEMATIC_REPORTING LIKE '%PNR%'
            OR C.PROCESS_PROBLEMATIC_REPORTING = 'Conflict Stale'
        )
),

SELLER_INCOMING AS (
    SELECT
        B.SIT_SITE_ID AS SITE,
        B.PERIODO,
        PP.ORD_SELLER_ID AS SELLER_ID,
        COUNT(DISTINCT B.CLA_CLAIM_ID) AS INCOMING
    FROM BASE_CONTACTS B
    LEFT JOIN `meli-bi-data.WHOWNER.DM_CX_POST_PURCHASE` PP
        ON PP.CLA_CLAIM_ID = B.CLA_CLAIM_ID
    GROUP BY 1, 2, 3
),

RANKED AS (
    SELECT
        SITE,
        PERIODO,
        SELLER_ID,
        INCOMING,
        ROW_NUMBER() OVER (PARTITION BY SITE, PERIODO ORDER BY INCOMING DESC) AS RNK
    FROM SELLER_INCOMING
),

SELLER_NAMES AS (
    SELECT DISTINCT
        O.ORD_SELLER.ID AS ORD_SELLER_ID,
        O.SIT_SITE_ID,
        O.ORD_SELLER.NICKNAME AS ORD_SELLER_NICKNAME
    FROM `meli-bi-data.WHOWNER.BT_ORD_ORDERS` O
    WHERE O.SIT_SITE_ID IN ('MLB')
        AND O.ORD_CREATED_DT >= '2025-11-01'
        AND O.ORD_CREATED_DT < '2026-01-01'
)

SELECT
    R.SITE,
    R.PERIODO,
    R.SELLER_ID,
    COALESCE(SN.ORD_SELLER_NICKNAME, 'SIN_NOMBRE') AS SELLER_NICKNAME,
    R.INCOMING,
    R.RNK
FROM RANKED R
LEFT JOIN SELLER_NAMES SN
    ON R.SELLER_ID = SN.ORD_SELLER_ID
    AND R.SITE = SN.SIT_SITE_ID
WHERE R.RNK <= 10
ORDER BY R.SITE, R.PERIODO, R.RNK

LIMIT 10000
