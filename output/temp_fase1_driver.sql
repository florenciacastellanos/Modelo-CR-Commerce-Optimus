-- FASE 1.2: DRIVER por período - Órdenes MLA (filtrado por site)
-- Dic 2025 vs Ene 2026
-- Según DRIVERS_BY_CATEGORY.md: Marketplace usa órdenes FILTRADAS por site

SELECT 
    FORMAT_DATE('%Y-%m', ORD.ORD_CLOSED_DT) AS MES,
    COUNT(DISTINCT ORD.ORD_ORDER_ID) AS DRIVER
FROM `meli-bi-data.WHOWNER.BT_ORD_ORDERS` ORD
WHERE 
    ORD.ORD_CLOSED_DT BETWEEN '2025-12-01' AND '2026-01-31'
    AND ORD.ORD_GMV_FLG = TRUE
    AND ORD.ORD_MARKETPLACE_FLG = TRUE
    AND ORD.SIT_SITE_ID = 'MLA'  -- ✅ Filtrado por site (Marketplace)
    AND UPPER(ORD.DOM_DOMAIN_ID) <> 'TIPS'
GROUP BY 1
ORDER BY 1
