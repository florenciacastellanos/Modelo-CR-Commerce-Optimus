-- Muestra de conversaciones PNR para seller 1046986303
-- 60 conversaciones de Dic 2025 + 40 de Nov 2025

WITH CONTACTS_SELLER AS (
    SELECT
        C.CLA_CLAIM_ID,
        C.CAS_CASE_ID,
        C.CONTACT_DATE_ID,
        DATE_TRUNC(C.CONTACT_DATE_ID, MONTH) AS PERIODO,
        ROW_NUMBER() OVER (
            PARTITION BY DATE_TRUNC(C.CONTACT_DATE_ID, MONTH)
            ORDER BY RAND()
        ) AS rn
    FROM `meli-bi-data.WHOWNER.BT_CX_CONTACTS` C
    LEFT JOIN `meli-bi-data.WHOWNER.DM_CX_POST_PURCHASE` PP
        ON PP.CLA_CLAIM_ID = C.CLA_CLAIM_ID
    WHERE C.SIT_SITE_ID = 'MLB'
        AND C.CONTACT_DATE_ID >= '2025-11-01'
        AND C.CONTACT_DATE_ID < '2026-01-01'
        AND C.PROCESS_BU_CR_REPORTING IN ('ME', 'ML')
        AND COALESCE(C.FLAG_EXCLUDE_NUMERATOR_CR, 0) = 0
        AND C.CLA_CLAIM_ID IS NOT NULL
        AND (
            C.PROCESS_PROBLEMATIC_REPORTING LIKE '%PNR%'
            OR C.PROCESS_PROBLEMATIC_REPORTING = 'Conflict Stale'
        )
        AND PP.ORD_SELLER_ID = 1046986303
),

SAMPLED AS (
    SELECT * FROM CONTACTS_SELLER
    WHERE (PERIODO = '2025-12-01' AND rn <= 60)
       OR (PERIODO = '2025-11-01' AND rn <= 40)
),

STUDIO AS (
    SELECT
        ST.CAS_CASE_ID,
        TRIM(REGEXP_REPLACE(
            CONCAT(
                COALESCE(JSON_VALUE(ST.SUMMARY_CX_STUDIO, '$.problem'), ''),
                ' ',
                COALESCE(JSON_VALUE(ST.SUMMARY_CX_STUDIO, '$.solution'), '')
            ),
            r'\s+', ' '
        )) AS CONVERSATION_SUMMARY
    FROM `meli-bi-data.WHOWNER.BT_CX_STUDIO_SAMPLE` ST
    WHERE ST.ARRIVAL_DATE BETWEEN '2025-10-01' AND '2026-01-31'
)

SELECT
    S.PERIODO,
    S.CAS_CASE_ID,
    S.CONTACT_DATE_ID,
    ST.CONVERSATION_SUMMARY
FROM SAMPLED S
LEFT JOIN STUDIO ST ON S.CAS_CASE_ID = ST.CAS_CASE_ID
WHERE ST.CONVERSATION_SUMMARY IS NOT NULL
  AND LENGTH(TRIM(ST.CONVERSATION_SUMMARY)) > 10
ORDER BY S.PERIODO, S.CONTACT_DATE_ID

LIMIT 200
